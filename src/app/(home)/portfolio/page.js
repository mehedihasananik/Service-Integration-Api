import Container from "@/Components/Container/Container";
import PortfolioPage from "@/Components/PagesComponents/PortfolioPage/PortfolioPage";
import {
  search_sevice_categoryAll,
  serviceApi,
  sevice_portfolioApi,
} from "@/config/apis";
export const metadata = {
  title: "Envobyte || Portfolio ",
  description: "Generated by create next app",
};

async function portfolioCategory() {
  const res = await fetch(`${sevice_portfolioApi}`, {
    next: { revalidate: 10 },
  });

  if (!res.ok) {
    throw new Error("Failed to fetch data");
  }
  return res.json();
}
async function servicesApi() {
  const res = await fetch(`${serviceApi}`, {
    next: { revalidate: 10 },
  });

  if (!res.ok) {
    throw new Error("Failed to fetch data");
  }
  return res.json();
}
async function serviceCategoriesApi() {
  const res = await fetch(`${serviceApi}`, {
    next: { revalidate: 10 },
  });

  if (!res.ok) {
    throw new Error("Failed to fetch data");
  }
  return res.json();
}

const Portfolio = async () => {
  // Fetch data for the page

  const portfolioCategories = await portfolioCategory();
  const services = await servicesApi();
  const serviceCategories = await serviceCategoriesApi();

  return (
    <div>
      <Container>
        <PortfolioPage
          portfolioCategories={portfolioCategories}
          services={services}
          serviceCategories={serviceCategories}
        />
      </Container>
    </div>
  );
};

export default Portfolio;
