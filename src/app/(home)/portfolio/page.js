import Container from "@/Components/Container/Container";
import PortfolioPage from "@/Components/PagesComponents/PortfolioPage/PortfolioPage";
import {
  search_sevice_categoryAll,
  serviceApi,
  sevice_portfolioApi,
} from "@/config/apis";
export const metadata = {
  title: "Envobyte || Portfolio ",
  description: "Generated by create next app",
};

const Portfolio = async () => {
  // Fetch data for the page
  const portfolioCategories = await fetch(`${sevice_portfolioApi}`).then(
    (res) => res.json()
  );
  const services = await fetch(`${serviceApi}`).then((res) => res.json());
  const serviceCategories = await fetch(`${search_sevice_categoryAll}`).then(
    (res) => res.json()
  );
  console.log(serviceCategories);

  return (
    <div>
      <Container>
        <PortfolioPage
          portfolioCategories={portfolioCategories}
          services={services}
          serviceCategories={serviceCategories}
        />
      </Container>
    </div>
  );
};

export default Portfolio;
